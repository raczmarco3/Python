def draw_table(q):
    print('+' + '-----------------' + '+')
    t = [['.', '.', '.', '.', '.', '.', '.', '.'], ['.', '.', '.', '.', '.', '.', '.', '.'],  ['.', '.', '.', '.', '.', '.', '.', '.'],
      ['.', '.', '.', '.', '.', '.', '.', '.'],  ['.', '.', '.', '.', '.', '.', '.', '.'],  ['.', '.', '.', '.', '.', '.', '.', '.'],
        ['.', '.', '.', '.', '.', '.', '.', '.'],  ['.', '.', '.', '.', '.', '.', '.', '.'], ]

    k = 0
    while(k < 8):
        l = 0
        while(l < 8):
            a = 7 - q[k]
            if(l == a):
                t[a][k] = "Q"
            l = l + 1
        k = k + 1


    for i in t:
        print('|', end = " ")
        for j in i:
            print(j, end = " ")
        print('|', end = " ")
        print()

    print('+' + '-----------------' + '+')


def main():
    draw_table([0,4,7,5,2,6,1,3])
      

#############################################################################

if __name__ == '__main__':
    main()